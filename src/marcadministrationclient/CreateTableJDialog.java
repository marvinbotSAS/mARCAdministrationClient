        public void DeleteAFieldTable()
    {
        if ( FieldsjTable.getSelectedRow() == -1  )
        {
            return;
        }
        int numberOfrows = FieldsjTable.getSelectedRowCount();
        int row = -1;
        DefaultTableModel m = (DefaultTableModel) FieldsjTable.getModel();
        for (int i = 0; i < numberOfrows; i++)
        {
            row = FieldsjTable.getSelectedRows()[i];
            m.removeRow(row);
        }
    }
