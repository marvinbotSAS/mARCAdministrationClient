    public void AddAFieldTable()
    {
            NewFieldJDialog d = new NewFieldJDialog();
            d.setModal(true);
            d.setLocationRelativeTo(FieldsjTable);
            d.setVisible(true);
            if ( d.state.equals("Ok") )
            {
                DefaultTableModel m = (DefaultTableModel) FieldsjTable.getModel();
                m.addRow(new String[] {d.NamejTextField.getText(),d.type,d.SizejTextField.getText()});
            }
    }
