    private void jButton1MouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseReleased
        String fields = "";
        DefaultTableModel m = (DefaultTableModel) FieldsjTable.getModel();
        String type = "";
        for (int i = 0; i < m.getRowCount();i++)
        {
            type = (String) m.getValueAt(i, 1);
            fields += (String) m.getValueAt(i, 0)+" "+type;
            if ( type.equals("CHAR"))
            {
                fields += " "+(String) m.getValueAt(i, 2);
            }
            if ( i != m.getRowCount() - 1)
            {
                fields +=", ";
            }
        }
        this.name = this.TableNamejTextField.getText();
        this.fields = fields;
        this.state = "Ok";
        dispose();
    }//GEN-LAST:event_jButton1MouseReleased

