    private void jCheckBoxMouseReleased(java.awt.event.MouseEvent evt) {                                         

        if ( !(evt.getSource() instanceof JCheckBox) )
        {
            return;
        }
        JCheckBox theCheckBox = (JCheckBox) evt.getSource();
        String name = "textArea_"+theCheckBox.getText();
            // on checke qu'il n'est pas déjà affiché
        Component comp = this.FindTextAreaFromAccessibleName(this, name);
        if ( theCheckBox.isSelected() )
        {
            if ( comp == null )
            {
                javax.swing.JTextArea a = new javax.swing.JTextArea();
                a.getAccessibleContext().setAccessibleName("textArea_"+theCheckBox.getText());
                Point p = theCheckBox.getLocation();
                a.setLocation(p.x, p.y+50);
                a.setSize(new Dimension(608,120));
                this.add(a);
                this.revalidate();
                this.repaint();
            }
            else
            {
             ((JTextArea)comp).setEditable(true);
             comp.setEnabled(true);
            }

        }
        else
        {
            ((JTextArea)comp).setEditable(false);
            comp.setEnabled(false);
        }
        
    }     
    public Component FindTextAreaFromAccessibleName(Component comp, String name)  
{  
  if(comp instanceof JTextArea)  
  {  
    String accName = ((JTextArea) comp).getAccessibleContext().getAccessibleName();  
    if(accName.equals(name)) 
    {
        return comp;
    }  
  }  
  if (comp instanceof Container)  
  {  
    Component[] comps = ((Container)comp).getComponents();  
    for(int x = 0, y = comps.length; x < y; x++)  
    {  
      FindTextAreaFromAccessibleName(comps[x],name);  
    }  
  }  
  
  return null;
}  
